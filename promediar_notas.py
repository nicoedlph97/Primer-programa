
#Promedio de notas

'''
N1 = primera nota
N2 = Segunda nota
N3 = Tercera nota

'''

N1 = float(input('Ingrese la primer nota: '))
N2 = float(input('Ingrese la segunda nota: '))
N3 = float(input('Ingrese la tercer nota: '))

Promedio = (N1 + N2 + N3)/3

print("El promedio de las notas es: "+str(Promedio)+' puntos.')

